value1 = float(input('Digite o primeiro valor: '))
value2 = float(input('Digite o segundo valor: '))

if value1 > value2:
    print('O PRIMEIRO valor é maior!')
elif value2 > value1:
    print('O SEGUNDO valor é maior!')
else:
    print('Ambos os valores são IGUAIS!')



